package es.juntadeandalucia.panelGestion.negocio.vo;

import java.io.Serializable;

public class ServiceVO implements Serializable {

   /**
    * Generated serial version UID
    */
   private static final long serialVersionUID = 9025493310582741853L;
   
   private String name;
   private String url;
   private String testServiceUrl;
   
   public ServiceVO() {}

   /**
    * @return the name
    */
   public String getName() {
      return name;
   }

   /**
    * @param name the name to set
    */
   public void setName(String name) {
      this.name = name;
   }

   /**
    * @return the url
    */
   public String getUrl() {
      return url;
   }

   /**
    * @param url the url to set
    */
   public void setUrl(String url) {
      this.url = url;
   }

   /**
    * @return the testServiceUrl
    */
   public String getTestServiceUrl() {
      return testServiceUrl;
   }

   /**
    * @param testServiceUrl the testServiceUrl to set
    */
   public void setTestServiceUrl(String testServiceUrl) {
      this.testServiceUrl = testServiceUrl;
   }
}
